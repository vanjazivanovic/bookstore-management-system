    private void btnOdjavaActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnOdjavaActionPerformed
        int result = JOptionPane.showConfirmDialog(this, "Da li ste sigurni da želite da se odjavite iz sistema?",
                "Potvrda", JOptionPane.YES_NO_OPTION);

        if (result == JOptionPane.NO_OPTION) {
            return;
        }

        if (result == JOptionPane.YES_OPTION) {
            try {
                KlijentKontroler.getInstance().odjava(ulogovani);
                JOptionPane.showMessageDialog(
                        this,
                        "Uspešno ste se odjavili.",
                        "Uspešno",
                        JOptionPane.INFORMATION_MESSAGE
                );
                new LoginForma().setVisible(true);
                Sesija.getInstance().setUlogovani(null);
                this.dispose();
            } catch (Exception ex) {
                Logger.getLogger(KlijentskaForma.class.getName()).log(Level.SEVERE, null, ex);

                JOptionPane.showMessageDialog(
                        this,
                        "Odjava nije uspela.",
                        "Greška",
                        JOptionPane.ERROR_MESSAGE
                );
            }
        }
    }//GEN-LAST:event_btnOdjavaActionPerformed

    private void txtPretragaKupcaKeyReleased(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_txtPretragaKupcaKeyReleased

        String tekst = txtPretragaKupca.getText().trim();

        if (tekst.equals("Unesite ime ili prezime...")) {
            return;
        }

        tekst = tekst.toLowerCase();

        cmbKupac.removeAllItems();

        for (Kupac k : sviKupci) {

            String naziv = (k.getIme() + " " + k.getPrezime()).toLowerCase();

            if (naziv.contains(tekst)) {
                cmbKupac.addItem(k);
            }
        }

    }//GEN-LAST:event_txtPretragaKupcaKeyReleased

    private void btnSAcuvajActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSAcuvajActionPerformed
        try {
            if (txtDatum.getText().isEmpty()) {
                JOptionPane.showMessageDialog(
                        this,
                        "Unesite datum izdavanja računa.",
                        "Upozorenje",
                        JOptionPane.WARNING_MESSAGE
                );
                return;
            }
            Kupac kupac = (Kupac) cmbKupac.getSelectedItem();
            SimpleDateFormat sdf = new SimpleDateFormat("dd.MM.yyyy");
            Date datumIsporuke = sdf.parse(txtDatum.getText());
            ModelTabeleStavkaRacuna mt = (ModelTabeleStavkaRacuna) tblStavkeRacuna.getModel();

            Racun racun = new Racun(-1, datumIsporuke, ukupanIznos, kupac, ulogovani, mt.getLista());

            KlijentKontroler.getInstance().sacuvajRacun(racun);

            txtDatum.setText("");
            txtUkupanIznos.setText("");
            ukupanIznos = 0;
            mt.getLista().clear();
            mt.fireTableDataChanged();
            JOptionPane.showMessageDialog(
                    this,
                    "Račun je uspešno sačuvan.",
                    "Uspešno",
                    JOptionPane.INFORMATION_MESSAGE
            );

            txtDatum.setText(sdf.format(new Date()));
            osveziKupce(null);
            osveziKnjige(null);
        } catch (Exception ex) {
            JOptionPane.showMessageDialog(
                    this,
                    "Račun nije moguće sačuvati.",
                    "Greška",
                    JOptionPane.ERROR_MESSAGE
            );
        }
    }//GEN-LAST:event_btnSAcuvajActionPerformed

