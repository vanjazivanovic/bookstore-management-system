    private void osveziKupce(Kupac selektovani) {

        cmbKupac.removeAllItems();

        for (Kupac k : sviKupci) {
            cmbKupac.addItem(k);
        }

        if (selektovani != null) {
            cmbKupac.setSelectedItem(selektovani);
        }

        txtPretragaKupca.setText("");
    }

    private void osveziKnjige(Knjiga selektovana) {

        cmbKnjiga.removeAllItems();

        for (Knjiga k : sveKnjige) {
            cmbKnjiga.addItem(k);
        }

        if (selektovana != null) {
            cmbKnjiga.setSelectedItem(selektovana);
        }

        txtPretragaKnjige.setText("");
    }
