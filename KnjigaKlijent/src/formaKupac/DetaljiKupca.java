    private void popuniMesta() {
        try {
            ArrayList<Mesto> mesta = KlijentKontroler.getInstance().vratiSvaMesta();

            cmbMesto1.removeAllItems();

            for (Mesto mesto : mesta) {
                cmbMesto1.addItem(mesto);
            }

        } catch (Exception ex) {
            Logger.getLogger(NoviKupac.class.getName()).log(Level.SEVERE, null, ex);
        }
    }
