    private void btnObrisiActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnObrisiActionPerformed
        int result = JOptionPane.showConfirmDialog(
                this,
                "Da li ste sigurni da želite da obrišete ovu knjigu?",
                "Potvrda",
                JOptionPane.YES_NO_OPTION,
                JOptionPane.QUESTION_MESSAGE);

        if (result == JOptionPane.NO_OPTION) {
            return;
        }

        if (result == JOptionPane.YES_OPTION) {
            try {
                KlijentKontroler.getInstance().obrisiKnjiga(knjiga);
                PretragaKnjiga pk = (PretragaKnjiga) getParent();
                pk.refresujTabalu();
                JOptionPane.showMessageDialog(
                        this,
                        "Knjiga je uspešno obrisana.",
                        "Uspešno",
                        JOptionPane.INFORMATION_MESSAGE
                );
                this.dispose();
            } catch (Exception ex) {
                JOptionPane.showMessageDialog(
                        this,
                        "Knjigu nije moguće obrisati.",
                        "Greška",
                        JOptionPane.ERROR_MESSAGE
                );
//                Logger.getLogger(FormDetaljiVino.class.getName()).log(Level.SEVERE, null, ex);
            }
        }
    }//GEN-LAST:event_btnObrisiActionPerformed

    private void btnIzmeniActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnIzmeniActionPerformed
        try {
            if (txtOpis.getText().trim().isEmpty()) {

                JOptionPane.showMessageDialog(
                        this,
                        "Unesite opis knjige.",
                        "Upozorenje",
                        JOptionPane.WARNING_MESSAGE
                );
                return;
            }
            String isbn = txtISBN.getText().trim();
            String izdavackaKuca = txtIzdavackaKuca.getText().trim();
            if (isbn.isEmpty()) {
                JOptionPane.showMessageDialog(
                        this,
                        "Unesite ISBN knjige.",
                        "Upozorenje",
                        JOptionPane.WARNING_MESSAGE
                );
                return;
            }

            if (izdavackaKuca.isEmpty()) {
                JOptionPane.showMessageDialog(
                        this,
                        "Unesite izdavačku kuću.",
                        "Upozorenje",
                        JOptionPane.WARNING_MESSAGE
                );
                return;
            }
            String opis = txtOpis.getText();
            if (txtCena.getText().trim().isEmpty()) {

                JOptionPane.showMessageDialog(
                        this,
                        "Unesite cenu knjige.",
                        "Upozorenje",
                        JOptionPane.WARNING_MESSAGE
                );
                return;
            }
            double cena;

            try {
                cena = Double.parseDouble(txtCena.getText());
            } catch (NumberFormatException ex) {

                JOptionPane.showMessageDialog(
                        this,
                        "Cena mora biti broj.",
                        "Upozorenje",
                        JOptionPane.WARNING_MESSAGE
                );
                return;
            }
            knjiga.setIsbn(isbn);
            knjiga.setIzdavackaKuca(izdavackaKuca);
            knjiga.setOpis(opis);
            knjiga.setCena(cena);

            KlijentKontroler.getInstance().promeniKnjiga(knjiga);
            PretragaKnjiga pk = (PretragaKnjiga) getParent();
            pk.refresujTabalu();

            JOptionPane.showMessageDialog(
                    this,
                    "Podaci o knjizi su uspešno izmenjeni.",
                    "Uspešno",
                    JOptionPane.INFORMATION_MESSAGE
            );
            this.dispose();

        } catch (Exception ex) {
            ex.printStackTrace();
            JOptionPane.showMessageDialog(
                    this,
                    "Podatke o knjizi nije moguće izmeniti.",
                    "Greška",
                    JOptionPane.ERROR_MESSAGE
            );
        }
    }//GEN-LAST:event_btnIzmeniActionPerformed

